/* See also RFC 4493, section 4, Example 1 */
void aes_cmac_test_0(void) {
  struct utils_aes_cc_ctx cc_aes;

  uint8_t key[] = {
      0x2b, 0x7e, 0x15, 0x16, 0x28, 0xae, 0xd2, 0xa6, 0xab, 0xf7, 0x15, 0x88, 0x09, 0xcf, 0x4f, 0x3c,
  };

  ss_utils_aes_cc_setup(&cc_aes, key, sizeof(key), false);
  printf("AES-CMAC feed m: (nothing, NULL)\n");
  ss_utils_aes_cc_feed(&cc_aes, NULL, 0, true);
  ss_utils_aes_cc_cleanup(&cc_aes);
  printf("AES-CMAC checksum: %s\n", ss_hexdump(cc_aes.cbc, sizeof(cc_aes.cbc)));
}

