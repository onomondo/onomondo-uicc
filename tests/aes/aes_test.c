void aes_cmac_test_two_blocks(void) {
  struct utils_aes_cc_ctx cc_aes;

  uint8_t block_1[] = {
		0x00, 0x28, 0x15, 0x1e, 0x19, 0x32, 0x32, 0xb0,
		0x00, 0x11, 0x00, 0x00, 0x00, 0x00, 0x01, 0x06,
	};

	uint8_t block_2[] = {
		0x00, 0xa4, 0x00, 0x04, 0x02, 0x3f, 0x00, 0x00,
		0xc0, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00
	};

	uint8_t key[] = {
		0x01, 0x23, 0x45, 0x67, 0x89, 0xab, 0xcd, 0xef,
		0x01, 0x23, 0x45, 0x67, 0x01, 0x23, 0x45, 0x67,
	};

  ss_utils_aes_cc_setup(&cc_aes, key, sizeof(key), true);
	printf("AES-CMAC feed block 1: %s\n", ss_hexdump(block_1, sizeof(block_1)));
	ss_utils_aes_cc_feed(&cc_aes, block_1, sizeof(block_1), false);
	printf("AES-CMAC feed block 2: %s\n", ss_hexdump(block_2, sizeof(block_2)));
	ss_utils_aes_cc_feed(&cc_aes, block_2, sizeof(block_2), true);
	ss_utils_aes_cc_cleanup(&cc_aes);
	printf("AES-CMAC checksum: %s\n", ss_hexdump(cc_aes.cbc, sizeof(cc_aes.cbc)));
}

